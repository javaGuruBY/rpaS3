package Iv.javaguru.demo;

public class MathOperationsDemo {
    public static void main(String [] args){
        //Addition
        int a = 100;
        int b = 45;
        int resualtAB =  a + b;
        System.out.println("1) Addition '+'");
        System.out.println("a = "+ a +","+"b = "+b);
        System.out.println("a + b = "+ a +" + "+ b +" = "+resualtAB);
        //Substuction
        int x = 40;
        int y = 18;
        int resualtXY =  x - y;
        System.out.println("'2) Substuction '-'");
        System.out.println("x = "+ x +","+"y = "+y);
        System.out.println("x - y = "+ x +" - "+ y +" = "+resualtXY);
        //Division
        int m = 15;
        int n = 3;
        int resualtMN =  m / n;
        System.out.println("3) Division '/'");
        System.out.println("m = "+ m +","+"n = "+n);
        System.out.println("m / n = "+ m +" / "+ n +" = "+resualtMN);
        //Remainder
        int z = 123;
        int c = 54;
        int resualtZC =  z * c;
        System.out.println("4) Remainder '*'");
        System.out.println("z = "+ z +","+"c = "+c);
        System.out.println("z * c = "+ z +" * "+ c +" = "+resualtZC);

    }
}
